for(let index = 1; index <= 10; index++) {
    if(index % 2 === 0) {
        console.log(index +' is Even');
    }
    else {
        console.log(index +' is Odd');
    }
}

for(let age = 1; age <= 18; age = age + 1) {
    if(age <= 1) {
        console.log('You are '+ age +' year old');
    }
    else {
        console.log('You are '+ age +' years old');
    }
}